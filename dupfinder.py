# dupFinder.py
import os, sys
import hashlib

# Scan a directory for duplicate files
#   returns a dictionary with the duplicate files
#   if traversing several dirs, need joinDicts to merge two dictionaries
def findDup(parentFolder):
    # Dups in format {hash:[names]}
    dups = {}
    for dirName, subdirs, fileList in os.walk(parentFolder):
        print('Scanning %s...' % dirName)
        for filename in fileList:
            # Get the path to the file
            path = os.path.join(dirName, filename)
            # Calculate hash
            file_hash = hashfile(path)
            # Add or append the file path
            if file_hash in dups:
                dups[file_hash].append(path)
            else:
                dups[file_hash] = [path]
    return dups
 

# Joins two dictionaries
# Iterates over second, see if key exists in first
#    if exists, appends values in second to first
#    if DNE, stores in first
# at end, dict1 contains all information
def joinDicts(dict1, dict2):
    for key in dict2.keys():
        if key in dict1:
            dict1[key] = dict1[key] + dict2[key]
        else:
            dict1[key] = dict2[key]
 
# Function to calculate has of given file.  Rx path, Tx HEX digest
def hashfile(path, blocksize = 65536):
    afile = open(path, 'rb')
    #hasher = hashlib.md5()
    hasher = hashlib.sha256()    
    buf = afile.read(blocksize)
    while len(buf) > 0:
        hasher.update(buf)
        buf = afile.read(blocksize)
    afile.close()
    return hasher.hexdigest()
 
# Prints the results
def printResults(dict1):
    results = list(filter(lambda x: len(x) > 1, dict1.values()))
    if len(results) > 0:
        print('Duplicates Found:')
        print('The following files are identical. The name could differ, but the content is identical')
        print('___________________')
        for result in results:
            for subresult in result:
                print('\t\t%s' % subresult)
            print('___________________')
 
    else:
        print('No duplicate files found.')
 
# To run script from cmd line, must rx folders as parameters, then call findDup for every folder 
# To run use python dupFinder.py /folder1 ./folder2
if __name__ == '__main__':
    if len(sys.argv) > 1:
        dups = {}
        folders = sys.argv[1:]
        for i in folders:
            # Iterate the folders given
            if os.path.exists(i):
                # Find the duplicated files and append them to the dups
                joinDicts(dups, findDup(i))
            else:
                print('%s is not a valid path, please verify' % i)
                sys.exit()
        printResults(dups)
    else:
        print('Usage: python dupFinder.py folder or python dupFinder.py "folder1" "folder2" "folder3"')